const LANG_KEY = 'rios.lang';
let _lang = (() => { try { return localStorage.getItem(LANG_KEY) || 'en'; } catch { return 'en'; } })();
const _listeners = new Set();

export const i18n = {
  get lang() { return _lang; },
  get isRTL() { return _lang === 'ar'; },
  set(lang) {
    if (lang !== 'en' && lang !== 'ar') return;
    _lang = lang;
    try { localStorage.setItem(LANG_KEY, lang); } catch {}
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    for (const fn of _listeners) try { fn(lang); } catch (e) { console.error(e); }
  },
  toggle() { this.set(_lang === 'ar' ? 'en' : 'ar'); },
  subscribe(fn) { _listeners.add(fn); return () => _listeners.delete(fn); },
};

export function t(key, vars) {
  const table = STRINGS[_lang] || STRINGS.en;
  let out = table[key] ?? STRINGS.en[key] ?? key;
  if (vars) for (const [k, v] of Object.entries(vars)) out = out.replaceAll('{' + k + '}', String(v));
  return out;
}

i18n.set(_lang);
